//  && And Operator

console.log(20 == 20 && 200 == 200); // true && true = True
console.log(20 == 20 && 200 == 230); // true && false = False
console.log(20 == 10 && 200 == 200); // false && true = False
console.log(20 == 10 && 230 == 200); // false && false = False



// || Or Operator

console.log(20 == 20 || 200 == 200); // true && true = True
console.log(20 == 20 || 200 == 230); // true && false = True
console.log(20 == 10 || 200 == 200); // false && true = Trur
console.log(20 == 10 || 230 == 200); // false && false = False





