let Day = 4

switch(Day){
    case 1 :
        document.write("Day-1 Monday")
        break;
    case 2 :
        document.write("Day-2 Tuesday")
        break;
    case 3 :
        document.write("Day-3 Wednesday")
        break;
    case 4 :
        document.write("Day-4 Thursday")
        break;
    case 5 :
        document.write("Day-5 Friday")
        break;
    case 6 :
        document.write("Day-6 Saturday")
        break;
    case 7 :
        document.write("Day-7 Sunday")
        break;
    default:
        document.write("Invalid Day Number")
        break;
}